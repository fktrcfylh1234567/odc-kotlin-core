// Sim Private Key
val A_SPK = alicePair.second

// Wallet ID
val A_WID = "W:2359780346"

// ---------------------------------------------------------------------------------------------------------------------
// ---------------------------------------------------------------------------------------------------------------------
// B -- Bob (Боб)

val bobPair = Crypto.initPair()

